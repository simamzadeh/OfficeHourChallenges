# Challenge: Packages into Lockers!
# Difficulty: Intermediate
#
# Amazon is developing a new locker system for Amazon deliveries to ensure customer packages are delivered in a secure location.
# In order to assist delivery drivers, Amazon will develop software to be installed that will instruct which locker the driver should place the package in depending on its size.
#
# For the purposes of this question, design and implement the component of this system that will take a package's size and return the locker number the package should be stored in so that the customer can be notified where their package is located.
# This solution should be able to simulate adding a package to the locker and removing a package given a locker number.